#include "Util.h"

#include <cstdio>

using namespace std;

string Util::Exec(const char* cmd) {
    FILE* pipe = popen(cmd, "r");
    if (!pipe) return "ERROR";
    char buffer[128];
    std::string result = "";
    while (!feof(pipe)) {
        if (fgets(buffer, 128, pipe) != NULL)
            result += buffer;
    }
    pclose(pipe);
    return result;
}

string Util::Exec(std::string& cmd) {
    return Exec(cmd.c_str());
}
